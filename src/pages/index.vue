                        <h2><strong>- Building up Complex intellects From Scratch with Wetware Engineering drived by Computer Science and -</strong></h2>
                        <p>
                            My dream is build up autonomous decision-making agents from scratch. <br>
                            Humans have been trying to create intellects from ancient era. Especially since the invention of computers, the development of automata and artificial intelligence has become a reality and has excited us. Meanwhile, the line between intellect and non-intellect has become blurred.<br>
                            <br>
                            What exactly is intellect? I don't have answers yet...<br>
                            <br>
                            Much discussion of artificial intellects in recent years has been based on neural network models inspired by the brain. However, when considering the entire living organism, the brain is an integrated circuit with an extremely special high-order information processing mechanism, and only a limited number of living species have it.<br>
                            All living organisms interact with the external environment based on complex control networks, and much of their information processing is not under centralized control from the brain, but rather through autonomous interactions. There are also countless examples of biological system that do not have brains that are intricately controlled by mysterious information processing networks.<br>
                            Therefore, I believe that future discussions on intellect should begin with control systems possessed by living organisms other than the brain.<br>
                            <br>
                            Since 2000, advances in synthetic biology have demonstrated that biological systems can be designed artificially. I am particularly impressed by the implementation of complex information processing systems through gene network design. On the other hand, past research has also been based on binary information processing based on ideas that were relatively strongly influenced by Neumann computers.<br>
                            <br>
                            By complicating and Darwinian-evolving analog computational systems derived from natural organisms, I aim to break through the classical computational concepts of synthetic biology and recreate more intellect systems that life originally possesses. I think it is possible.<br>
                            <br>
                            Of course, classical computers and artificial intelligence based on neural networks can greatly assist human abilities. Therefore, by interacting the knowledge of calculations in wetware with classical computers, it seems that more applied thinking will become possible.<br>
                            <br>
                            I approach the essence of intellect by gradually increasing the complexity of primitive information processing based on biological systems derived from natural organisms, Darwinian evolution, and the power of computer science.
